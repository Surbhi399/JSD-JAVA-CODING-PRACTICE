
public class DemoOfLocalVariables {
	public static void main (String args [])
	{
		int a=0;
		float f =0.0f;
		char c ='C';
		double d=0.0d;
		boolean b = true;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d);
		System.out.println(f);
	}
}
